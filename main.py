    """Обработка введённой даты рождения"""
    date_str = message.text.strip()

    try:
        # Проверяем формат даты
        datetime.strptime(date_str, "%d.%m.%Y")

        # Рассчитываем число
        number = calculate_from_date(date_str)

        if number in MASTER_NUMBERS:
            desc = MASTER_NUMBERS[number]
            response = f"🔮 Твоё число судьбы: {number} (Мастер-число)\n\n"
            response += f"*{desc['name']}*\n\n"
            response += f"{desc['description']}\n\n"
            response += f"⚠️ *Проблема:* {desc['problem']}\n\n"
            response += f"🌟 *Знаменитости:* {desc['celebrities']}"
        else:
            response = f"🔢 Твоё число судьбы: {number}\n\n"
            response += f"*{NUMBERS_MEANING[number]['name']}*\n\n"
            response += f"{SHORT_MEANING[number]}\n\n"
            response += f"💪 *Сильные стороны:*\n{NUMBERS_MEANING[number]['strengths']}\n\n"
            response += f"😔 *Слабые стороны:*\n{NUMBERS_MEANING[number]['weaknesses']}"

        await state.clear()
        await message.answer(response, parse_mode="Markdown", reply_markup=after_calculation_keyboard(number))

    except ValueError:
        await message.answer(
            "❌ Неверный формат даты. Пожалуйста, используй формат ДД.ММ.ГГГГ (например, 15.07.1985):",
            reply_markup=back_to_main_keyboard()
        )


