def reduce_to_number(num: int) -> int:
    """Сводит число к значению от 1 до 9 или определяет мастер-число"""
    if num in [11, 22, 33]:
        return num

    while num > 9:
        num = sum(int(digit) for digit in str(num))
    return num


def calculate_from_date(date_str: str) -> int:
    """Рассчитывает число судьбы из даты рождения"""
    # Удаляем разделители
    clean_str = date_str.replace('.', '').replace(' ', '').replace('-', '')

    if not clean_str.isdigit():
        raise ValueError("Дата должна содержать только цифры и разделители")

    total = sum(int(digit) for digit in clean_str)
    return reduce_to_number(total)


def calculate_from_name(name: str) -> int:
    """Рассчитывает число имени"""
    name = name.lower().replace(' ', '')
    total = 0

    for char in name:
        if char in LETTER_VALUES:
            total += LETTER_VALUES[char]

    return reduce_to_number(total)


